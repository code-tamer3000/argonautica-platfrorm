export function HeaderDeco() {
  const enabled = useShipDecoStore((s) => s.enabled)
  if (!enabled) return null

  return (
    <div className={styles.deco} aria-hidden>
      {/* Волна B (линии) — нижний слой */}
      <div className={`${styles.waves} ${styles.waveB}`}>
        <div className={`${styles.track} ${styles.trackB}`}>
          {Array.from({ length: CURL_B_COUNT }, (_, i) => (
            <span
              key={i}
              className={`${styles.curl} ${styles.curlB}`}
              // Противофаза у соседей + бегущий сдвиг фазы.
              style={{ animationDelay: `${((i % 2 === 0 ? 0 : -2.75) + (i % 4) * -0.3).toFixed(2)}s` }}
            >
              <CurlBSvg />
            </span>
          ))}
        </div>
      </div>

      {/* Волна A (завитки) — над линиями */}
      <div className={`${styles.waves} ${styles.waveA}`}>
        <div className={`${styles.track} ${styles.trackA}`}>
          {Array.from({ length: CURL_A_COUNT }, (_, i) => (
            <span
              key={i}
              className={`${styles.curl} ${styles.curlA}`}
              style={{ animationDelay: `${((i % 2 === 0 ? 0 : -2.1) + (i % 4) * -0.28).toFixed(2)}s` }}
            >
              <CurlASvg />
            </span>
          ))}
        </div>
      </div>

      {/* Кораблик — поверх обеих волн */}
      <div className={styles.ship}>
        <div className={styles.hull}><ShipSvg /></div>
      </div>
    </div>
  )
}
